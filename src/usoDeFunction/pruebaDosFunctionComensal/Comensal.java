package usoDeFunction.pruebaDosFunctionComensal;

/**
 *
 * @author diego
 */
public class Comensal {

    String nombre;
    double totalResultado;
    int mesa;

    public Comensal() {
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public double getTotalResultado() {
        return totalResultado;
    }

    public void setTotalResultado(double totalResultado) {
        this.totalResultado = totalResultado;
    }

    public int getMesa() {
        return mesa;
    }

    public void setMesa(int mesa) {
        this.mesa = mesa;
    }

}
